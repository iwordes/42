#include <main.h>

void	mx_mul(t_mx m1, t_mx m2, t_mx o)
{
	uint8_t	x;
	uint8_t	y;

	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			o[y][x] = m1[y][0] * m2[0][x]
					+ m1[y][1] * m2[1][x]
					+ m1[y][2] * m2[2][x]
					+ m1[y][3] * m2[3][x];
			x += 1;
		}
		y += 1;
	}
}
