#include <main.h>

void	mx_cpy(t_mx dest, t_mx src)
{
	uint8_t		x;
	uint8_t		y;

	y = ~0;
	while (++y < 4)
	{
		x = ~0;
		while (++x < 4)
			dest[y][x] = src[y][x];
	}
}
