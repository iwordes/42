#include <main.h>

void	mx_mulv(t_vec *v, t_mx mx)
{
	v->x = v->x * mx[0][0]
		+ v->x * mx[0][1]
		+ v->x * mx[0][2]
		+ v->x * mx[0][3];
	v->y = v->y * mx[1][0]
		+ v->y * mx[1][1]
		+ v->y * mx[1][2]
		+ v->y * mx[1][3];
	v->z = v->z * mx[2][0]
		+ v->z * mx[2][1]
		+ v->z * mx[2][2]
		+ v->z * mx[2][3];
}
